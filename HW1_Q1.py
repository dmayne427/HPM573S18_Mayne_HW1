x = 17

# integer
y1 = x
print('y1 is a ' ,type(y1))
print('y1 = ',y1)

# float
y2 = float(x)
print('y2 is a ' ,type(y2))
print('y2 = ',y2)

# string
y3 = str(x)
print('y3 is a ' ,type(y3))
print('y3 = ',y3)

# Boolean, '17 > 16'
y4 = bool(x > 16)
print('y4 is a ' ,type(y4))
print('y4 = ',y4)

text = 'The ' + 'value ' + 'of ' + 'x ' + 'is ' + y3 + '.'
print(text)
